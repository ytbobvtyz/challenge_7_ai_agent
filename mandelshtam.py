def сады(этап=0):
    if этап == 6:
        return "сердце дрожит"

    # Базовый вызов (первая строфа)
    основа = "Любимая, в садах умирающих"

    # Рекурсивный шаг: добавляется новый слой, но сохраняется память о предыдущем
    if этап == 0:
        return основа + ", в садах, \n" + сады(этап=1)
    elif этап == 1:
        return основа + ", где лист, как письмо, желтеет, \n" + сады(этап=2)
    elif этап == 2:
        return основа + ", где ветер, как палец, дрожит, \n" + сады(этап=3)
    elif этап > 2:
        return "Я слышу звон колокольный, \n" + сады(этап=+1)


# Запуск:
сады()